package EstruturaSequencial;

public class ExBasico0 {
    public static void main(String[] args) {
        // Bloco A
        int x, y;
        x = 5;
        y = 2 * x;

        // Bloco B
        int a;
        double b;
        a = 5;
        b = 2 * a;


        // Bloco A
        System.out.println(x); // 5
        System.out.println(y); // 10

        // Bloco B
        System.out.println(a); // 5
        System.out.println(b); // 10.0

    }
}
